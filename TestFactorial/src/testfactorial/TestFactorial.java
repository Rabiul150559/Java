
package testfactorial;


public class TestFactorial {

    
    public static void main(String[] args) {
        int facValue=4;
        int factorial=1;
        for(int i=facValue; i>1; i--){
            factorial *=i;
            System.out.println("Factorial is " + factorial);
        
        
        }
        
        
        
    }
    
}
